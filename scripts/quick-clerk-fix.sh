#!/bin/bash

# 快速Clerk域名修复脚本
# 提供两种方案供选择

clear

cat << 'EOF'
╔══════════════════════════════════════════════════════════════╗
║                                                              ║
║           Clerk 域名配置修复工具                             ║
║                                                              ║
╚══════════════════════════════════════════════════════════════╝

问题诊断:
  当前配置域名: clerk.alphapercept.co (.co)
  实际生产域名: www.alphapercept.com (.com)
  ❌ 域名不匹配 → 认证失败 (401错误)

EOF

echo ""
echo "请选择修复方案:"
echo ""
echo "┌────────────────────────────────────────────────────────────┐"
echo "│                                                            │"
echo "│  方案A: 在现有Clerk应用中添加域名（推荐，最简单）         │"
echo "│  ─────────────────────────────────────────────            │"
echo "│  • 不需要更改密钥                                          │"
echo "│  • 只需在Clerk Dashboard点击几下                           │"
echo "│  • 2-3分钟完成                                             │"
echo "│                                                            │"
echo "│  步骤:                                                     │"
echo "│    1. 访问 https://dashboard.clerk.com/                    │"
echo "│    2. Settings → Domains                                   │"
echo "│    3. 添加 www.alphapercept.com                            │"
echo "│    4. 添加 alphapercept.com                                │"
echo "│    5. 保存                                                 │"
echo "│                                                            │"
echo "└────────────────────────────────────────────────────────────┘"
echo ""
echo "┌────────────────────────────────────────────────────────────┐"
echo "│                                                            │"
echo "│  方案B: 创建新的Clerk应用（如果方案A不可用）              │"
echo "│  ─────────────────────────────────────────────            │"
echo "│  • 需要获取新的密钥                                        │"
echo "│  • 需要更新Vercel环境变量                                  │"
echo "│  • 需要重新部署                                            │"
echo "│  • 10-15分钟完成                                           │"
echo "│                                                            │"
echo "│  步骤:                                                     │"
echo "│    1. 创建新的Clerk应用                                    │"
echo "│    2. 获取新密钥                                           │"
echo "│    3. 运行自动化脚本更新配置                               │"
echo "│                                                            │"
echo "└────────────────────────────────────────────────────────────┘"
echo ""

read -p "请选择 (A/B): " choice

case $choice in
    [Aa])
        clear
        cat << 'EOF'
╔══════════════════════════════════════════════════════════════╗
║                                                              ║
║           方案A: 在Clerk Dashboard添加域名                   ║
║                                                              ║
╚══════════════════════════════════════════════════════════════╝

请按照以下步骤操作:

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
步骤 1: 打开Clerk Dashboard
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  1. 在浏览器中访问:
     👉 https://dashboard.clerk.com/

  2. 使用你的账户登录

  3. 你应该看到应用列表，找到你的应用
     (可能叫 "Alpha-Quant-Copilot" 或类似名称)

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
步骤 2: 进入Domains设置
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  左侧菜单中找到:

    ⚙️  Settings (点击展开)
       ├─ General
       ├─ 🌐 Domains          👈 点击这里
       ├─ Security
       └─ ...

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
步骤 3: 添加域名
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  在Domains页面，你会看到:

    Authorized Domains 或 Allowed Origins
    ┌───────────────────────────────────────┐
    │ clerk.alphapercept.co    [Active]     │
    │                                       │
    │ [+ Add domain] 或 [Add origin]        │ 👈 点击这里
    └───────────────────────────────────────┘

  添加第一个域名:
    📝 输入: www.alphapercept.com
    ✅ 点击 "Add" 或 "Save"

  添加第二个域名:
    📝 输入: alphapercept.com
    ✅ 点击 "Add" 或 "Save"

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
步骤 4: 验证配置
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  添加成功后，你应该看到:

    Authorized Domains
    ┌───────────────────────────────────────┐
    │ clerk.alphapercept.co    [Active]     │
    │ www.alphapercept.com     [Active] ✅  │
    │ alphapercept.com         [Active] ✅  │
    └───────────────────────────────────────┘

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
步骤 5: 测试修复
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

  配置生效后（等待1-2分钟）:

  1️⃣  清除浏览器缓存
      Chrome: Ctrl+Shift+Delete → 清除Cookie和缓存

  2️⃣  访问生产网站
      https://www.alphapercept.com

  3️⃣  重新登录

  4️⃣  测试添加自选股
      ✅ 应该不再报 401 错误
      ✅ 应该显示 "已添加到自选股"

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

完成后按任意键退出...
EOF
        read -n 1
        ;;

    [Bb])
        echo ""
        echo "正在启动自动化配置脚本..."
        echo ""
        sleep 2
        exec ./scripts/update-clerk-production.sh
        ;;

    *)
        echo ""
        echo "❌ 无效的选择"
        exit 1
        ;;
esac
